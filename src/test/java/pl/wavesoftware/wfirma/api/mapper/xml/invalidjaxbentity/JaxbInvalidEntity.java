package pl.wavesoftware.wfirma.api.mapper.xml.invalidjaxbentity;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.adapters.XmlAdapter;
import javax.xml.bind.annotation.adapters.XmlJavaTypeAdapter;
import org.joda.money.Money;

/**
 *
 * @author Krzysztof Suszyński <krzysztof.suszynski@wavesoftware.pl>
 */
@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement
public class JaxbInvalidEntity {

    @XmlElement
    @XmlJavaTypeAdapter(InvalidMoneyToStringAdapter.class)
    private Money price;

    @XmlElement
    private String name;

    public Money getPrice() {
        return price;
    }

    public void setPrice(Money price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public static class InvalidMoneyToStringAdapter extends XmlAdapter<String, Money> {

        @Override
        public Money unmarshal(String v) throws Exception {
            throw new IllegalArgumentException("Not supported, on purpuse.");
        }

        @Override
        public String marshal(Money v) throws Exception {
            throw new IllegalArgumentException("Not supported, on purpuse.");
        }

    }
}
